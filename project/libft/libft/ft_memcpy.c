#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*ptrdst;
	char	*ptrsrc;

	ptrdst = (char *)dst;
	ptrsrc = (char *)src;
	if (!dst && !src)
	{
		return (0);
	}
	while (n--)
	{
		ptrdst[n] = ptrsrc[n];
	}
	return (dst);
}
